using System.ComponentModel.Design;
using System.Runtime.Intrinsics.X86;
{ 
var number = 1; 


{
    


    if (number % 15 == 0)
    {
        Console.WriteLine("FizzBuzz");
    }
    else if (number % 5 == 0)
    {
        Console.WriteLine("Buzz");
    }
    else if (number % 3 == 0)
    {
        Console.WriteLine("Fizz");
    }
    else
    {
        Console.WriteLine(number);
